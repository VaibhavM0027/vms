import 'dart:io';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_storage/firebase_storage.dart';
import 'package:flutter/material.dart';
import 'package:image_picker/image_picker.dart';
import 'package:connectivity_plus/connectivity_plus.dart';
import '../widgets/qr_code_widget.dart';

class VisitorSelfRegisterScreen extends StatefulWidget {
  const VisitorSelfRegisterScreen({super.key});

  @override
  State<VisitorSelfRegisterScreen> createState() => _VisitorSelfRegisterScreenState();
}

class _VisitorSelfRegisterScreenState extends State<VisitorSelfRegisterScreen> {
  final _formKey = GlobalKey<FormState>();
  final _nameController = TextEditingController();
  final _contactController = TextEditingController();
  final _emailController = TextEditingController();
  final _hostNameController = TextEditingController();
  final _purposeController = TextEditingController();
  File? _photo;
  final ImagePicker _picker = ImagePicker();
  bool _isSubmitting = false;
  String? _lastGeneratedQR; // Store the last generated QR code

  Future<void> _pickPhoto() async {
    try {
      final x = await _picker.pickImage(source: ImageSource.camera);
      if (x != null) {
        setState(() => _photo = File(x.path));
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error capturing photo: $e')),
        );
      }
    }
  }

  Future<void> _submit() async {
    if (!_formKey.currentState!.validate()) return;
    if (_photo == null) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Please capture a photo')),
      );
      return;
    }

    setState(() => _isSubmitting = true);

    try {
      // Check if a registered visitor already exists by contact number
      final existingVisitorQuery = await FirebaseFirestore.instance
          .collection('visitors')
          .where('contact', isEqualTo: _contactController.text.trim())
          .where('isRegistered', isEqualTo: true)
          .limit(1)
          .get();
      
      String qrCode;
      String visitorId;
      
      if (existingVisitorQuery.docs.isNotEmpty) {
        // Existing registered visitor found - reuse their QR code
        final existingDoc = existingVisitorQuery.docs.first;
        final existingData = existingDoc.data();
        visitorId = existingDoc.id;
        qrCode = existingData['qrCode'] ?? visitorId;
        
        // Add new visit to their history
        final now = DateTime.now();
        List<Map<String, dynamic>> history = existingData['visitHistory'] != null 
            ? List.from(existingData['visitHistory'])
            : [];
            
        final newVisit = {
          'checkIn': now,
          'checkOut': null,
          'purpose': _purposeController.text.trim(),
          'hostId': '',
          'hostName': _hostNameController.text.trim(),
          'status': 'pending',
          'visitDate': now,
        };
        history.add(newVisit);
        
        // Update visitor document with new visit
        await FirebaseFirestore.instance
            .collection('visitors')
            .doc(visitorId)
            .update({
          'name': _nameController.text.trim(),
          'email': _emailController.text.trim(),
          'hostName': _hostNameController.text.trim(),
          'purpose': _purposeController.text.trim(),
          'visitDate': now,
          'checkIn': now,
          'checkOut': null,
          'status': 'pending',
          'visitHistory': history,
          'updatedAt': FieldValue.serverTimestamp(),
        });
        
      } else {
        // New visitor - create with permanent QR code
        final now = DateTime.now();
        final docRef = FirebaseFirestore.instance.collection('visitors').doc();
        visitorId = docRef.id;
        qrCode = docRef.id;

        final visitorData = {
          'name': _nameController.text.trim(),
          'contact': _contactController.text.trim(),
          'email': _emailController.text.trim(),
          'hostName': _hostNameController.text.trim(),
          'purpose': _purposeController.text.trim(),
          'hostId': '',
          'visitDate': now,
          'checkIn': now,
          'checkOut': null,
          'meetingNotes': null,
          'status': 'pending',
          'qrCode': qrCode,
          'photoUrl': null,
          'isRegistered': true, // Mark as registered for permanent QR
          'visitHistory': [{
            'checkIn': now,
            'checkOut': null,
            'purpose': _purposeController.text.trim(),
            'hostId': '',
            'hostName': _hostNameController.text.trim(),
            'status': 'pending',
            'visitDate': now,
          }],
          'createdAt': FieldValue.serverTimestamp(),
        };

        await docRef.set(visitorData);
      }

      if (!mounted) return;

      setState(() {
        _isSubmitting = false;
        _lastGeneratedQR = qrCode; // Store the QR code
      });
      _showQRCodeDialog(qrCode, existingVisitorQuery.docs.isNotEmpty);

      // Upload photo
      () async {
        try {
          final connectivityResult = await Connectivity().checkConnectivity();
          if (connectivityResult == ConnectivityResult.none) {
            if (mounted) {
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(content: Text('No internet connection. Photo will be uploaded when online.')),
              );
            }
            return;
          }
          
          final ref = FirebaseStorage.instance
              .ref()
              .child('visitor_photos/$visitorId.jpg');
          await ref.putFile(_photo!);
          final url = await ref.getDownloadURL();
          await FirebaseFirestore.instance
              .collection('visitors')
              .doc(visitorId)
              .update({'photoUrl': url});
        } on FirebaseException catch (e) {
          if (mounted) {
            if (e.code == 'unauthenticated' || e.code == 'permission-denied') {
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(content: Text('Photo upload requires authentication. Please contact reception.')),
              );
            } else {
              ScaffoldMessenger.of(context).showSnackBar(
                SnackBar(content: Text('Photo upload failed: ${e.message}')),
              );
            }
          }
        } catch (e) {
          if (mounted) {
            ScaffoldMessenger.of(context).showSnackBar(
              SnackBar(content: Text('Photo upload failed: $e')),
            );
          }
        }
      }();

    } catch (e) {
      if (mounted) {
        setState(() => _isSubmitting = false);
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error registering: $e')),
        );
      }
    }
  }

  void _showQRCodeDialog(String qrData, [bool isExistingVisitor = false]) {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (_) => AlertDialog(
        backgroundColor: Colors.grey[850],
        title: Text(
          isExistingVisitor ? 'Welcome Back!' : 'Registration Successful!',
          style: const TextStyle(color: Colors.white),
        ),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Text(
              isExistingVisitor 
                  ? 'Your permanent QR Code is ready. You can reuse this QR code for future visits!'
                  : 'Your permanent QR Code has been generated. Save this for future visits!',
              style: TextStyle(color: Colors.grey[300]),
            ),
            const SizedBox(height: 20),
            Container(
              padding: const EdgeInsets.all(16),
              decoration: BoxDecoration(
                border: Border.all(color: Colors.grey),
                borderRadius: BorderRadius.circular(8),
                color: Colors.white,
              ),
              child: CustomQRCodeWidget(
                data: qrData,
                size: 200,
                backgroundColor: Colors.white,
                foregroundColor: Colors.black,
                errorMessage: 'QR Code Unavailable',
              ),
            ),
            const SizedBox(height: 16),
            Text(
              'Visitor ID: ${qrData.substring(0, 8).toUpperCase()}',
              style: const TextStyle(
                color: Colors.white,
                fontWeight: FontWeight.bold,
              ),
            ),
            if (isExistingVisitor)
              Padding(
                padding: const EdgeInsets.only(top: 8),
                child: Text(
                  'New visit registered successfully!',
                  style: TextStyle(
                    color: Colors.green[300],
                    fontWeight: FontWeight.bold,
                  ),
                ),
              ),
          ],
        ),
        actions: [
          TextButton(
            onPressed: () {
              Navigator.pop(context);
              _showCheckoutOption(qrData);
            },
            child: const Text(
              'Done',
              style: TextStyle(color: Colors.blue),
            ),
          ),
        ],
      ),
    );
  }

  void _showCheckoutOption(String qrData) {
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (_) => AlertDialog(
        backgroundColor: Colors.grey[850],
        title: const Text(
          'Registration Complete!',
          style: TextStyle(color: Colors.white, fontSize: 20, fontWeight: FontWeight.bold),
        ),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            Icon(
              Icons.hourglass_empty,
              size: 48,
              color: Colors.orange[300],
            ),
            const SizedBox(height: 16),
            Text(
              'Your visit request is pending approval.',
              style: TextStyle(color: Colors.grey[300], fontSize: 16),
              textAlign: TextAlign.center,
            ),
            const SizedBox(height: 8),
            Text(
              'You will receive a notification when your visit is approved or rejected. You can check your status anytime.',
              style: TextStyle(color: Colors.grey[400], fontSize: 14),
              textAlign: TextAlign.center,
            ),
          ],
        ),
        actions: [
          TextButton(
            onPressed: () {
              Navigator.pop(context);
              // Don't navigate away, just close the dialog to show the floating action button
            },
            child: Text(
              'Stay Here',
              style: TextStyle(color: Colors.grey[400], fontSize: 16),
            ),
          ),
          ElevatedButton(
            onPressed: () {
              Navigator.pop(context);
              _navigateToStatus(qrData);
            },
            style: ElevatedButton.styleFrom(
              backgroundColor: Colors.blue[700],
              foregroundColor: Colors.white,
              padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 12),
            ),
            child: const Text(
              'Check Status',
              style: TextStyle(fontSize: 16, fontWeight: FontWeight.bold),
            ),
          ),
        ],
      ),
    );
  }


  void _navigateToStatus(String qrData) {
    Navigator.pushNamed(context, '/visitor-status', arguments: {'visitorId': qrData});
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.black,
      appBar: AppBar(
        title: const Text('Self Registration'),
        backgroundColor: Colors.black,
        elevation: 0,
      ),
      floatingActionButton: _lastGeneratedQR != null
          ? FloatingActionButton.extended(
              onPressed: () => _navigateToStatus(_lastGeneratedQR!),
              backgroundColor: Colors.blue[700],
              foregroundColor: Colors.white,
              icon: const Icon(Icons.info),
              label: const Text('Check Status'),
            )
          : null,
      body: Container(
        decoration: BoxDecoration(
          gradient: LinearGradient(
            begin: Alignment.topCenter,
            end: Alignment.bottomCenter,
            colors: [Colors.black, Colors.grey[900]!, Colors.black],
          ),
        ),
        child: SingleChildScrollView(
          padding: const EdgeInsets.all(20),
          child: Form(
            key: _formKey,
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Container(
                  width: double.infinity,
                  padding: const EdgeInsets.all(20),
                  decoration: BoxDecoration(
                    color: Colors.grey[850]!,
                    borderRadius: BorderRadius.circular(16),
                    boxShadow: [
                      BoxShadow(
                        color: Colors.grey[800]!.withValues(alpha: 0.3),
                        blurRadius: 8,
                        offset: const Offset(0, 2),
                      )
                    ],
                  ),
                  child: Column(
                    children: [
                      Icon(Icons.person_add, size: 48, color: Colors.grey[300]),
                      const SizedBox(height: 16),
                      Text(
                        'Visitor Self Registration',
                        style: TextStyle(
                            fontSize: 24,
                            color: Colors.grey[100],
                            fontWeight: FontWeight.bold),
                      ),
                      const SizedBox(height: 8),
                      Text(
                        'Register yourself as a visitor',
                        style: TextStyle(fontSize: 16, color: Colors.grey[400]),
                      ),
                    ],
                  ),
                ),
                const SizedBox(height: 24),

                // Photo Section
                Container(
                  width: double.infinity,
                  padding: const EdgeInsets.all(20),
                  decoration: BoxDecoration(
                    color: Colors.grey[850]!,
                    borderRadius: BorderRadius.circular(16),
                    boxShadow: [
                      BoxShadow(
                        color: Colors.grey[800]!.withValues(alpha: 0.3),
                        blurRadius: 8,
                        offset: const Offset(0, 2),
                      )
                    ],
                  ),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        'Photo Capture',
                        style: TextStyle(
                            fontSize: 18,
                            color: Colors.grey[100],
                            fontWeight: FontWeight.bold),
                      ),
                      const SizedBox(height: 8),
                      Text(
                        'Photo is mandatory for registration',
                        style: TextStyle(
                            fontSize: 12,
                            color: Colors.grey[400],
                            fontStyle: FontStyle.italic),
                      ),
                      const SizedBox(height: 16),
                      Center(
                        child: GestureDetector(
                          onTap: _pickPhoto,
                          child: Container(
                            width: 120,
                            height: 120,
                            decoration: BoxDecoration(
                              color: Colors.grey[800]!,
                              borderRadius: BorderRadius.circular(12),
                              border: Border.all(color: Colors.grey[600]!, width: 2),
                            ),
                            child: _photo != null
                                ? ClipRRect(
                              borderRadius: BorderRadius.circular(10),
                              child: Image.file(
                                _photo!,
                                fit: BoxFit.cover,
                              ),
                            )
                                : Column(
                              mainAxisAlignment: MainAxisAlignment.center,
                              children: [
                                Icon(Icons.camera_alt,
                                    size: 32, color: Colors.grey[400]),
                                const SizedBox(height: 8),
                                Text(
                                  'Tap to capture',
                                  style: TextStyle(
                                      color: Colors.grey[400], fontSize: 12),
                                ),
                              ],
                            ),
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
                const SizedBox(height: 24),

                // Form Fields
                Container(
                  width: double.infinity,
                  padding: const EdgeInsets.all(20),
                  decoration: BoxDecoration(
                    color: Colors.grey[850]!,
                    borderRadius: BorderRadius.circular(16),
                    boxShadow: [
                      BoxShadow(
                        color: Colors.grey[800]!.withValues(alpha: 0.3),
                        blurRadius: 8,
                        offset: const Offset(0, 2),
                      )
                    ],
                  ),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        'Personal Information',
                        style: TextStyle(
                            fontSize: 18,
                            color: Colors.grey[100],
                            fontWeight: FontWeight.bold),
                      ),
                      const SizedBox(height: 8),
                      Text(
                        'Fields marked with * are mandatory',
                        style: TextStyle(
                            fontSize: 12,
                            color: Colors.grey[400],
                            fontStyle: FontStyle.italic),
                      ),
                      const SizedBox(height: 20),

                      // Name Field
                      TextFormField(
                        controller: _nameController,
                        style: TextStyle(color: Colors.grey[100]),
                        decoration: InputDecoration(
                          labelText: 'Full Name *',
                          labelStyle: TextStyle(color: Colors.grey[400]),
                          prefixIcon: Icon(Icons.person, color: Colors.grey[400]),
                          border: OutlineInputBorder(
                              borderRadius: BorderRadius.circular(12)),
                          enabledBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[600]!),
                          ),
                          focusedBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[300]!),
                          ),
                        ),
                        validator: (value) {
                          if (value == null || value.trim().isEmpty) {
                            return 'Please enter your name';
                          }
                          return null;
                        },
                      ),
                      const SizedBox(height: 16),

                      // Contact Field
                      TextFormField(
                        controller: _contactController,
                        style: TextStyle(color: Colors.grey[100]),
                        decoration: InputDecoration(
                          labelText: 'Contact Number *',
                          labelStyle: TextStyle(color: Colors.grey[400]),
                          prefixIcon: Icon(Icons.phone, color: Colors.grey[400]),
                          border: OutlineInputBorder(
                              borderRadius: BorderRadius.circular(12)),
                          enabledBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[600]!),
                          ),
                          focusedBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[300]!),
                          ),
                        ),
                        validator: (value) {
                          if (value == null || value.trim().isEmpty) {
                            return 'Please enter your contact number';
                          }
                          final phoneRegex = RegExp(r'^\+?[\d\s\-\(\)]{10,}$');
                          if (!phoneRegex.hasMatch(value.trim())) {
                            return 'Please enter a valid contact number';
                          }
                          return null;
                        },
                      ),
                      const SizedBox(height: 16),

                      // Email Field
                      TextFormField(
                        controller: _emailController,
                        style: TextStyle(color: Colors.grey[100]),
                        decoration: InputDecoration(
                          labelText: 'Email Address *',
                          labelStyle: TextStyle(color: Colors.grey[400]),
                          prefixIcon: Icon(Icons.email, color: Colors.grey[400]),
                          border: OutlineInputBorder(
                              borderRadius: BorderRadius.circular(12)),
                          enabledBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[600]!),
                          ),
                          focusedBorder: OutlineInputBorder(
                            borderRadius: BorderRadius.circular(12),
                            borderSide: BorderSide(color: Colors.grey[300]!),
                          ),
                        ),
                        validator: (value) {
                          if (value == null || value.trim().isEmpty) {
                            return 'Please enter your email address';
